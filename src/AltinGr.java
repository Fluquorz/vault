
public class AltinGr {
	
	private String name;
	private int AltinGrAdet;
	private Kur kur;
	
	
	public AltinGr(int altinGrAdet) {
		AltinGrAdet = altinGrAdet;
		
	}



	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}



	public int getAltinGrAdet() {
		return AltinGrAdet;
	}


	public void setAltinGrAdet(int altinGrAdet) {
		AltinGrAdet = altinGrAdet;
	}


	public Kur getKur() {
		return kur;
	}


	public void setKur(Kur kur) {
		this.kur = kur;
	}
	
	
	

}
