
public class AltinCeyrek {
	
	private String name;
	private int AltinCeyrekAdet;
	private Kur kur;
	
	
	
	public AltinCeyrek(int altinCeyrekAdet) {
		AltinCeyrekAdet = altinCeyrekAdet;
		
	}
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public int getAltinCeyrekAdet() {
		return AltinCeyrekAdet;
	}
	public void setAltinCeyrekAdet(int altinCeyrekAdet) {
		AltinCeyrekAdet = altinCeyrekAdet;
	}
	public Kur getKur() {
		return kur;
	}
	public void setKur(Kur kur) {
		this.kur = kur;
	}
	
	
	
}
