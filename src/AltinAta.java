
public class AltinAta {
	
	private String name;
	private int AltinAtaAdet;
	private Kur kur;
	
	public AltinAta(int altinAtaAdet) {
		AltinAtaAdet = altinAtaAdet;
		
	}
	
	

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}


	public int getAltinAtaAdet() {
		return AltinAtaAdet;
	}

	public void setAltinAtaAdet(int altinAtaAdet) {
		AltinAtaAdet = altinAtaAdet;
	}

	public Kur getKur() {
		return kur;
	}

	public void setKur(Kur kur) {
		this.kur = kur;
	}
	
	
	

}
